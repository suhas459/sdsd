package lp;

import java.util.Scanner;

public class evenOddNum {

	public static void main(String[] args) {
		
		
		Scanner sc= new Scanner(System.in);
		
		int value = sc.nextInt();
		
		
		if (value%2==0) {
			System.out.println("even number");
		}else {
			System.out.println("odd num");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
